import UIKit

extension UIColor {
    
    @nonobjc class var azure: UIColor {
        return UIColor(red: 0.0, green: 153.0 / 255.0, blue: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPink: UIColor {
        return UIColor(red: 240.0 / 255.0, green: 240.0 / 255.0, blue: 2440.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var primaryBlue: UIColor {
        return UIColor(red: 14.0 / 255.0, green: 130.0 / 255.0, blue: 199.0 / 255.0, alpha: 1.0)
    }
}
